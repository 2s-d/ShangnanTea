/**
 * 获取行政区划简称
 * @param {string} fullName - 完整的行政区划名称
 * @returns {string} - 简称
 */
export function getShortName(fullName) {
  if (!fullName) return ''
  
  // 1. 先查映射表（省级行政区）
  if (REGION_SHORT_NAME_MAP[fullName]) {
    return REGION_SHORT_NAME_MAP[fullName]
  }
  
  // 2. 处理地级市/自治州/盟/地区
  // 自治州：XX族自治州 → XX（去掉"族自治州"及民族名）
  // 例如：海北藏族自治州 → 海北，延边朝鲜族自治州 → 延边
  if (fullName.includes('族自治州')) {
    return fullName.replace(/(.+?)(?:各族|土家族苗族|藏族羌族|彝族|朝鲜族|哈萨克族|蒙古族|回族|藏族|苗族|布依族|侗族|瑶族|白族|傣族|黎族|畲族|壮族|傈僳族|土族|羌族|哈尼族|纳西族|景颇族|柯尔克孜族|达斡尔族|锡伯族|拉祜族|佤族|水族|东乡族|仫佬族|毛南族|京族|基诺族|撒拉族|保安族|裕固族|俄罗斯族|鄂温克族|鄂伦春族|赫哲族|门巴族|珞巴族|独龙族|怒族|普米族|阿昌族|德昂族|布朗族|塔吉克族|乌孜别克族|塔塔尔族|高山族)自治州$/, '$1')
  }
  
  // 自治州（无"族"字）：XX自治州 → XX
  if (fullName.endsWith('自治州')) {
    return fullName.replace(/自治州$/, '')
  }
  
  // 自治县：优先查映射表（避免正则误判民族名和地名）
  if (fullName.includes('自治县') || fullName.endsWith('自治县')) {
    // 先查完整映射表
    if (REGION_SHORT_NAME_MAP[fullName]) {
      return REGION_SHORT_NAME_MAP[fullName]
    }
    // 如果映射表没有，才用简单规则：XX自治县 → XX
    return fullName.replace(/自治县$/, '')
  }
  
  // 盟：XX盟 → XX
  if (fullName.endsWith('盟')) {
    return fullName.replace(/盟$/, '')
  }
  
  // 地区：XX地区 → XX
  if (fullName.endsWith('地区')) {
    return fullName.replace(/地区$/, '')
  }
  
  // 林区：神农架林区 → 神农架
  if (fullName.endsWith('林区')) {
    return fullName.replace(/林区$/, '')
  }
  
  // 普通地级市：XX市 → XX
  if (fullName.endsWith('市')) {
    return fullName.replace(/市$/, '')
  }
  
  // 其他情况原样返回
  return fullName
}

/**
 * 格式化现居地显示（省-市格式）
 * @param {string} locationCode - 存储的现居地编码，格式：省code-市code
 * @returns {string} - 格式化后的显示文本，如：陕西-商洛
 */
export function formatLocationDisplay(locationCode) {
  if (!locationCode) return ''
  
  const parts = locationCode.split('-')
  if (parts.length < 2) return ''
  
  // 从 CodeToText 获取完整名称
  const provinceFull = CodeToText[parts[0]] || parts[0]
  const cityFull = CodeToText[parts[1]] || parts[1]
  
  // 转换为简称
  const provinceShort = getShortName(provinceFull)
  const cityShort = getShortName(cityFull)
  
  return `${provinceShort}-${cityShort}`
}

